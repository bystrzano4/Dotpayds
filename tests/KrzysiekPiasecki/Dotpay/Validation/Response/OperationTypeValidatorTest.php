namespace KrzysiekPiasecki\Dotpay\Validation\Response;

use KrzysiekPiasecki\Dotpay\Validation\Response\Constraint\OperationTypeConstraint;
use Symfony\Component\Validator\Test\ConstraintValidatorTestCase;

/**
 * @coversDefaultClass \KrzysiekPiasecki\Dotpay\Validation\Response\OperationTypeValidator
 */
class OperationTypeValidatorTest extends ConstraintValidatorTestCase
{
    /**
     * @covers ::validate()
     * @dataProvider provideValidValues
     */
    public function testValidValue(string $value)
    {
        $constraint = new OperationTypeConstraint();
        $this->validator->validate($value, $constraint);
        $this->assertNoViolation();
    }

    /**
     * @covers ::validate()
     */
    public function testInvalidValue()
    {
        $this->markTestSkipped('Not implemented yet');
    }

    /**
     * @return array
     */
    public function provideValidValues(): array
    {
        return [
            ['payment'],
            ['payment_multimerchant_child'],
            ['payment_multimerchant_parent'],
            ['refund'],
            ['payout'],
            ['payout_any_amount'],
            ['release_rollback'],
            ['unidentified_payment'],
            ['complaint'],
            ['credit_card_registration'],
        ];
    }

    protected function createValidator()
    {
        return new OperationTypeValidator();
    }
}
